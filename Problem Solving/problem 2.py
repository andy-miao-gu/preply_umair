# a car has speed of 100 m/s how much time it will take to cover 10 kilometer distance. 
speed = int(input("say the speed of the car dont say anything like seconds hours minutes only numbers"))
distance = int(input("say the distance"))
print("it will take",speed*distance,"seconds")
print("it will take",(speed*distance)/60,"minutes")
print("it will take",(speed*distance)/(3600),"hours")